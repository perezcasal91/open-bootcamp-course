package task3.module;

public class Person {
    private int age;
    private String name;
    private String phone;
    public Person(){}
    public int getAge() {
        return age;
    }
    public String getName() {
        return name;
    }
    public String getPhone() {
        return phone;
    }
    public void setAge(int age) {
        this.age = age;
    }
    public void setName(String name) {
        this.name = name;
    }
    public void setPhone(String phone) {
        this.phone = phone;
    }
}
