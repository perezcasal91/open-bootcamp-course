package concepts;

public class DataTypes {

    // Variable
    // Within the assignation
    // type identifier = assassinated value;
    // Without the assignation
    // type identifier;

    // Primitive Types

    // Numbers
    byte aByte = 1;
    short aShort = 2;
    int anInt = 3;
    long aLong = 4l;

    // Float Point
    float aFloat = 1.2f;
    double aDouble = 1.3d;

    // Character
    char aChar = '1';

    // Booleans
    boolean aBoolean = true;

    // Wrapper Types = They can be null

    // Numbers
    Byte aWByte = 1;
    Short aWShort = 2;
    Integer anWInt = 3;
    Long aWLong = 4l;

    // Float Point
    Float aWFloat = 1.2f;
    Double aWDouble = 1.3d;

    // Character
    Character aWChar = '1';

    // Booleans
    Boolean aWBoolean = true;

    // Strings
    String aString = "This is a String";

}
